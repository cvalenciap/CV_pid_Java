package pe.com.sedapal.pide.model;

import java.io.Serializable;

import pe.com.sedapal.pide.model.base.Base;

public class PideGeneral extends Base implements Serializable {

	private static final long serialVersionUID = 1L;

	Integer idGeneral;
	String desGeneral;
	String desCorta;
	String stRegi;
	Integer idTipoParametro;
	String nomPgr;
	
	public Integer getIdGeneral() {
		return idGeneral;
	}
	public void setIdGeneral(Integer idGeneral) {
		this.idGeneral = idGeneral;
	}
	public String getDesGeneral() {
		return desGeneral;
	}
	public void setDesGeneral(String desGeneral) {
		this.desGeneral = desGeneral;
	}
	public String getDesCorta() {
		return desCorta;
	}
	public void setDesCorta(String desCorta) {
		this.desCorta = desCorta;
	}
	public String getStRegi() {
		return stRegi;
	}
	public void setStRegi(String stRegi) {
		this.stRegi = stRegi;
	}
	public Integer getIdTipoParametro() {
		return idTipoParametro;
	}
	public void setIdTipoParametro(Integer idTipoParametro) {
		this.idTipoParametro = idTipoParametro;
	}
	public String getNomPgr() {
		return nomPgr;
	}
	public void setNomPgr(String nomPgr) {
		this.nomPgr = nomPgr;
	}
	
	
	
}
